    private void lihatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lihatActionPerformed
        // TODO add your handling code here:
        int IdAntrian = Integer.parseInt(nomor_antrian.getText());
        try{
            Connection kon = KoneksiDB.getKoneksi();
            Statement stat = kon.createStatement();

            String sqlCari = "SELECT * FROM mendaftar INNER JOIN tb_pasien ON mendaftar.nik = tb_pasien.nik INNER JOIN antrian ON mendaftar.id_antrian = antrian.id_antrian INNER JOIN antrian ON tb_klinik.id_klinik = antrian.id_klinik INNER JOIN antrian ON tb_dokter.nama_dokter = antrian.nama_dokter WHERE antrian.id_antrian = \"+IdAntrian+";
            ResultSet r = stat.executeQuery(sqlCari);

            while(r.next()){
                String _namapasien = r.getString("tb_pasien.nama_lengkap");
                String _nik = r.getString("tb_pasien.nik");
                String _antrian = r.getString("antrian.id_antrian");
                String _namaklinik = r.getString("tb_klinik.nama_klinik");
                String _namadokter = r.getString("tb_dokter.nama_dokter");
                String tanggal = r.getString("antrian.waktu").substring(0, 10);

                DefaultTableModel model = (DefaultTableModel)tabel.getModel();

                Object []  dataYangDitampil = {_namapasien, _nik, _antrian, _namaklinik, _namadokter, tanggal};
                model.addRow(dataYangDitampil);

            }
            r.close();
            stat.close();

        } catch (SQLException e) {
            String pesan = "Terjadi Lesalahan Dalam Menampilkan Antrian";
            JOptionPane.showMessageDialog(null, pesan + e.getMessage());
        }
    }//GEN-LAST:event_lihatActionPerformed

