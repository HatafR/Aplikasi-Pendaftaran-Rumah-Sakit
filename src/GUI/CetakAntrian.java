    private void CetakTiketPDF(String _namapasien, String _nik, String _antrian, String _namaklinik, String _namadokter, String tanggal) {
        //Deklarasi halaman pdf baru
        PDPage Antrian = new PDPage();
        //pilih jenis dan ukuran font yang ingin dipakai
        PDFont JenisFont = PDType1Font.COURIER;
        int fontSize = 12;
        //Buat file pdf
        try (PDDocument dokumen = new PDDocument()) {
        //Buat halaman baru
        dokumen.addPage(Antrian);
        //Buat objek content yang akan diisi teks
        PDPageContentStream content= new PDPageContentStream(dokumen, Antrian);
        //Mulai menulis dokumen
        content.beginText();
        //Aktifkan font yang telah dipilih di awal
        content.setFont(JenisFont, fontSize);
        //Atur besarnya spasi antar baris
        content.setLeading(20f);
        //Atur posisi awal penulisan teks 

        content.newLineAtOffset(50, 750);
        //Menulis isi dokumen
        content.showText("Antrian Pasien");
        content.newLine();
        content.newLine(); //dua kali enter
        content.showText("Nama Pasien: " + _namapasien);
        content.newLine(); //pindah baris
        content.showText("NIK Pasien : " + _nik);
        content.newLine();
        content.showText("Antrian : " + _antrian);
        content.newLine();
        content.showText("Klinik : " + _namaklinik);
        content.newLine();
        content.showText("Dokter : " + _namadokter);
        content.newLine();
        content.showText("Tanggal : " + tanggal );
        //Mengakhiri penulisan dokumen
        content.endText();
        //Menutup dokumen
        content.close(); // Stream must be closed before saving document.
        //Menyimpan dokumen pada direktori tertentu
        dokumen.save("D:/DOCUMENTS/TiketKereta-01.pdf");
        } catch (IOException ioEx) {
        System.out.println("Gagal membuat pdf");
        }
    //Buka file pdf
    try {
        File file = new File("D:/DOCUMENTS/ANTRIAN-001.pdf");
        if (file.exists()) {
            Desktop.getDesktop().open(file);
        } else {
            System.out.println("File not exits -> " + file.getAbsolutePath());
        }
    } catch (IOException e) {
        System.out.println("Gagal membuka file pdf.");

    }}
